#include "libft.h"

void	ft_putnbr(int n)
{
	char digit;
	long nbr;

	nbr = (long)n;
	if (nbr < 0)
	{
		ft_putchar('-');
		nbr *= -1;
	}
	if (nbr > 9)
		ft_putnbr(nbr / 10);
	digit = nbr % 10;
	ft_putchar(digit + '0');
}
